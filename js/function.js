// Basic Function
function salam() {
    console.log("Hello Wolrd!");
}
salam();

// Expression Function
let salam2 = function () {
    console.log("Hello world!");
}
salam2();

// Arrow Function
var salam3 = () => {
    console.log("Hello World!");
}
var salam4 = () => console.log("Hello World!");
salam3();
salam4();